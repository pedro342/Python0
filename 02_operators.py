### Operadores ###

print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)
print(10 % 2)
print(3 // 4) # Floor division = aproximacion a un entero
print(2 ** 3) # Exponente = 2^3

# Concatenacion
print("Hola"+"Python")

# Comparaciones
print(3>4)
print(3<4)
print(3>=4)
print(3<=4)
print(3==4)
print(3!=4)

# Operadores logicos
print(3 > 4 and "hola" > "Python")
print(3 > 4 or "hola" > "Python")
print(not(3 > 4))